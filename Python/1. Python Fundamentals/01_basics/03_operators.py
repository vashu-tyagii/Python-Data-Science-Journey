# Operators
a = 10
b = 3

# Arithmetic
print(f"a + b = {a + b}")
print(f"a / b = {a / b}")  # Float division
print(f"a // b = {a // b}")  # Integer division

# Comparison
print(f"a > b is {a > b}")
print(f"a == b is {a == b}")  # type:ignore

# Logical
x = True
y = False
print(f"x and y is {x and y}")
print(f"x or y is {x or y}")
print(f"not x is {not x}")